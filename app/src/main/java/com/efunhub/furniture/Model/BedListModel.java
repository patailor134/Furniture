public class BedListModel
{
    String bedImage, bedName, bedFinish, bedPrice;

    public BedListModel() {
    }

    public String getBedImage() {
        return bedImage;
    }

    public void setBedImage(String bedImage) {
        this.bedImage = bedImage;
    }

    public String getBedName() {
        return bedName;
    }

    public void setBedName(String bedName) {
        this.bedName = bedName;
    }

    public String getBedFinish() {
        return bedFinish;
    }

    public void setBedFinish(String bedFinish) {
        this.bedFinish = bedFinish;
    }

    public String getBedPrice() {
        return bedPrice;
    }

    public void setBedPrice(String bedPrice) {
        this.bedPrice = bedPrice;
    }
}
