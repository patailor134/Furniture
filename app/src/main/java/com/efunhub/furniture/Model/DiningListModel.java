public class DiningListModel
{
    String diningImgage, diningName, diningFinish, diningPrice;

    public DiningListModel() {
    }

    public String getDiningImgage() {
        return diningImgage;
    }

    public void setDiningImgage(String diningImgage) {
        this.diningImgage = diningImgage;
    }

    public String getDiningName() {
        return diningName;
    }

    public void setDiningName(String diningName) {
        this.diningName = diningName;
    }

    public String getDiningFinish() {
        return diningFinish;
    }

    public void setDiningFinish(String diningFinish) {
        this.diningFinish = diningFinish;
    }

    public String getDiningPrice() {
        return diningPrice;
    }

    public void setDiningPrice(String diningPrice) {
        this.diningPrice = diningPrice;
    }
}
