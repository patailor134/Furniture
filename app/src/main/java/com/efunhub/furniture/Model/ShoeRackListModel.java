public class ShoeRackListModel
{
    String shoeRackImage, shoeRackName, shoeRackFinish, shoeRackPrice;

    public ShoeRackListModel() {
    }

    public String getShoeRackImage() {
        return shoeRackImage;
    }

    public void setShoeRackImage(String shoeRackImage) {
        this.shoeRackImage = shoeRackImage;
    }

    public String getShoeRackName() {
        return shoeRackName;
    }

    public void setShoeRackName(String shoeRackName) {
        this.shoeRackName = shoeRackName;
    }

    public String getShoeRackFinish() {
        return shoeRackFinish;
    }

    public void setShoeRackFinish(String shoeRackFinish) {
        this.shoeRackFinish = shoeRackFinish;
    }

    public String getShoeRackPrice() {
        return shoeRackPrice;
    }

    public void setShoeRackPrice(String shoeRackPrice) {
        this.shoeRackPrice = shoeRackPrice;
    }
}
