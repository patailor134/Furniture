public class SofaListModel
{
    String sofaImage, sofaName, sofaColor, sofaPrice;

    public SofaListModel() {
    }

    public String getSofaImage() {
        return sofaImage;
    }

    public void setSofaImage(String sofaImage) {
        this.sofaImage = sofaImage;
    }

    public String getSofaName() {
        return sofaName;
    }

    public void setSofaName(String sofaName) {
        this.sofaName = sofaName;
    }

    public String getSofaColor() {
        return sofaColor;
    }

    public void setSofaColor(String sofaColor) {
        this.sofaColor = sofaColor;
    }

    public String getSofaPrice() {
        return sofaPrice;
    }

    public void setSofaPrice(String sofaPrice) {
        this.sofaPrice = sofaPrice;
    }
}
