public class SofaSimilarListModel
{
    String sSimilarImage, sSimilarName, sSimilarColor, sSimilarPrice;

    public SofaSimilarListModel() {
    }

    public String getsSimilarImage() {
        return sSimilarImage;
    }

    public void setsSimilarImage(String sSimilarImage) {
        this.sSimilarImage = sSimilarImage;
    }

    public String getsSimilarName() {
        return sSimilarName;
    }

    public void setsSimilarName(String sSimilarName) {
        this.sSimilarName = sSimilarName;
    }

    public String getsSimilarColor() {
        return sSimilarColor;
    }

    public void setsSimilarColor(String sSimilarColor) {
        this.sSimilarColor = sSimilarColor;
    }

    public String getsSimilarPrice() {
        return sSimilarPrice;
    }

    public void setsSimilarPrice(String sSimilarPrice) {
        this.sSimilarPrice = sSimilarPrice;
    }
}
