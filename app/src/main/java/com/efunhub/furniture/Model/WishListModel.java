public class WishListModel
{
    String wishImages, wishName, wishStrikePrice, wishPrice;

    public WishListModel()
    {
    }

    public String getWishImages() {
        return wishImages;
    }

    public void setWishImages(String wishImages) {
        this.wishImages = wishImages;
    }

    public String getWishName() {
        return wishName;
    }

    public void setWishName(String wishName) {
        this.wishName = wishName;
    }

    public String getWishStrikePrice() {
        return wishStrikePrice;
    }

    public void setWishStrikePrice(String wishStrikePrice) {
        this.wishStrikePrice = wishStrikePrice;
    }

    public String getWishPrice() {
        return wishPrice;
    }

    public void setWishPrice(String wishPrice) {
        this.wishPrice = wishPrice;
    }
}
